import { useState } from "react";
import { IoIosHeartEmpty } from "react-icons/io";
import { FaHeart } from "react-icons/fa";

import { wishlist } from "../../services/services";
import { Link } from "react-router-dom";
import { useAuth } from "../../context/usercontext";
import { useLocation } from "react-router-dom";

export function Cards({ showproduct,ads }) {
  const [count, setCount] = useState(4);
  const { user, login } = useAuth();
  const location=useLocation()
  console.log("user///", user);
  console.log("ads",ads)
  return (
    <div className="bg-gradient-to-t from-gray-100 via-gray-50 to-white mt-[5vh]">
      <div className="mx-auto max-w-2xl px-4 py-16 sm:px-6 sm:py-24 lg:max-w-7xl lg:px-8">
        {/* <h2 className="text-xl font-bold ml-5 mb-3">Explore Products</h2> */}
        {/* ads */}
        {location.pathname === '/' ?
          <div className="flex justify-between items-center ml-10 mr-10 overflow-x-auto gap-10 h-[50vh] bg-sky-50 pl-10 pl-10 rounded-[20px] mb-10">
            {ads?.map((ads) => (
              <Link to={`/details/${ads.Productid._id}`}>
                <div className="shadow-sm shadow-gray-400 bg-white p-3 rounded-[10px]  hover:bg-sky-100 hover:text-sky-900 hover:shadow-md text-center h-[35vh] w-[10vw]">
                  {/* like */}
                  {user?.wishlist?.includes(ads.Productid._id) ? (
                    <FaHeart
                      className="relative top-7 left-[90%]  text-red-500 "
                      onClick={(e) => {
                        e.preventDefault();
                        wishlist(ads.Productid._id).then((data) => {
                          login(data.User);
                        });
                      }}
                    />
                  ) : (
                    <IoIosHeartEmpty
                      className="relative top-7 left-[90%] text-gray-600"
                      onClick={(e) => {
                        e.preventDefault();
                        wishlist(ads.Productid._id).then((data) => {
                          login(data.User);
                        });
                      }}
                    />
                  )}

                  <a key={ads.Productid.id} href={ads.Productid.href} className="group">
                    <img
                      src={ads.Productid.image[0]?.url}
                      className="aspect-square rounded-lg bg-gray-50 object-fill rounded-md"
                    />
                    <h3 className="mt-4 text-sm text-gray-700">{ads.Productid.name}</h3>
                    <p className="mt-1 text-lg font-medium text-gray-900">
                      {ads.Productid.price}&#x20b9;
                    </p>
                  </a>
                </div>
              </Link>
            ))}
          </div>
            :""}
        {/*  */}
        <h2 className="text-xl font-bold ml-5 mb-3">Explore Products</h2>

        <div className="grid grid-cols-2 gap-x-6 gap-y-10 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 xl:gap-x-20">
          {showproduct?.slice(0, count).map((product) => (
            <Link to={`/details/${product._id}`}>
              <div className="shadow-sm shadow-gray-400 bg-white p-3 rounded-[10px]  hover:bg-sky-100 hover:text-sky-900 hover:shadow-md text-center">
                {/* like */}
                {user?.wishlist?.includes(product._id) ? (
                  <FaHeart
                    className="relative top-7 left-[90%]  text-red-500 "
                    onClick={(e) => {
                      e.preventDefault();
                      wishlist(product._id).then((data) => {
                        login(data.User);
                      });
                    }}
                  />
                ) : (
                  <IoIosHeartEmpty
                    className="relative top-7 left-[90%] text-gray-600"
                      onClick={(e) => {
                      e.preventDefault();
                      wishlist(product._id).then((data) => {
                        login(data.User);
                      });
                    }}
                  />
                )}

                <a key={product.id} href={product.href} className="group">
                  <img
                    src={product?.image[0]?.url}
                    className="aspect-square rounded-lg bg-gray-50 object-fill rounded-md"
                  />
                  <h3 className="mt-4 text-sm text-gray-700">{product.name}</h3>
                  <p className="mt-1 text-lg font-medium text-gray-900">
                    {product.price}&#x20b9;
                  </p>
                  <p className="mt-1 text-[10px] font-medium relative left-22 font-bold text-gray-400">
                    {product.age}-year/old
                  </p>
                </a>
              </div>
            </Link>
          ))}
          <button
            className=" bg-black text-white text-center p-0"
            onClick={() => {
              setCount(count + 4);
            }}
          >
            More..
          </button>
        </div>
      </div>
    </div>
  );
}
